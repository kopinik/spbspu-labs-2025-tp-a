#include "HelpText.h"

namespace voronina
{
  const char* HelpText::getHelpText()
  {
    return R"(Шеннон-Фано: справка по командам

getcodes <входной_файл>
    Построить дерево Шеннона-Фано по тексту из файла
    и добавить кодировку в таблицу.

encode <входной_файл> <выходной_файл>
    Сжать текст из входного файла и сохранить результат в выходной файл.

decode <входной_файл> <выходной_файл> <номер_кодировки> <значащих_бит>
    Декодировать файл с помощью выбранной кодировки.
    <значащих_бит> — количество значащих бит в последнем байте.

printinfo <номер_кодировки>
    Вывести подробную информацию о выбранной кодировке.

compare <входной_файл> <количество_кодировок> <кодировка1> <кодировка2> ...
    Сравнить эффективность разных кодировок для одного файла.

entropy <входной_файл>
    Рассчитать энтропию текста в файле (бит/символ).

origin <номер_кодировки>
    Показать исходный файл, из которого была построена кодировка.

listcodes
    Вывести список всех сохранённых кодировок с краткой информацией.

codesinfo <количество_кодировок> <кодировка1> <кодировка2> ...
    Вывести подробную информацию о выбранных кодировках.

ablecoding <входной_файл> <количество_кодировок> <кодировка1> <кодировка2> ...
    Проверить, какими кодировками можно закодировать данный файл.

visualize
    Отобразить дерево Шеннона-Фано в виде ASCII-графики.

definite_encode <входной_файл> <выходной_файл> <номер_кодировки>
    Сжать текст выбранной кодировкой и сохранить результат в файл.

Ошибки:
    <INVALID COMMAND> — неверные аргументы
    <EMPTY входной_файл> — пустой файл
    <DECODING ERROR> — ошибка декодирования
)";
  }
}
